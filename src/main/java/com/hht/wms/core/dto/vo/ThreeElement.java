package com.hht.wms.core.dto.vo;

public class ThreeElement {
	
	private String po ; 
	
	private String so ; 
	
	private String sku ;

	public String getPo() {
		return po;
	}

	public void setPo(String po) {
		this.po = po;
	}

	public String getSo() {
		return so;
	}

	public void setSo(String so) {
		this.so = so;
	}

	public String getSku() {
		return sku;
	}

	public void setSku(String sku) {
		this.sku = sku;
	} 
	
	

}
